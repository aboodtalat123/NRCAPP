# نجيب المحتوى من قاعدة بيانات الزحف لو موجود ومفيد
crawled = {}
if os.path.exists(DB):
    conn = sqlite3.connect(DB)
    rows = conn.execute("SELECT url, title, content FROM pages").fetchall()
    conn.close()
    for url, title, content in rows:
        path = urlparse(url).path.rstrip("/") or "/"
        txt = content.strip() if content else ""
        if "الصفحة غير موجودة" not in txt and len(txt) > 50:
            if path not in crawled or len(txt) > len(crawled[path]):
                crawled[path] = txt

pages = []
for path, title in KNOWN_PATHS.items():
    content = crawled.get(path, "") or RICH.get(path, title)
    pages.append({
        "url": f"https://nrcapp.onrender.com{path}",
        "title": title,
        "content": content[:3000],
    })

with open(OUT, "w", encoding="utf-8") as f:
    json.dump(pages, f, ensure_ascii=False, indent=2)

print(f"[تم] {len(pages)} صفحة → {OUT}")
for p in pages:
    src = "مزحوف" if p['url'].rstrip("/") in {f"https://nrcapp.onrender.com{k}" for k in crawled} else "يدوي"
    print(f"  [{src}] {p['title']} ({len(p['content'])} حرف)")
